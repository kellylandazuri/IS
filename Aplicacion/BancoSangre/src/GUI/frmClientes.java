    private void verficarCampos()
    {
        String cedula, nombre, apellido, direccion, telefono, sistole, diastole, peso, talla;
        cedula=txtCedula.getText().trim();
        nombre= txtNombre.getText().trim();
        apellido = txtApellido.getText().trim();
        direccion = txtDireccion.getText().trim();
        telefono = txtTelefono.getText().trim();
        sistole = txtSistole.getText().trim();
        diastole = txtDiastole.getText().trim();
        peso = (txtPeso.getText().trim());
        talla = (txtTalla.getText().trim());
        
        if (cedula.isEmpty() || nombre.isEmpty() || apellido.isEmpty() || direccion.isEmpty() || telefono.isEmpty()
                || sistole.isEmpty() || diastole.isEmpty() || peso.isEmpty() || talla.isEmpty())
        {
            btnInsertar.setEnabled(false);
        }
        else{
            btnInsertar.setEnabled(true);
        }
    }
    
