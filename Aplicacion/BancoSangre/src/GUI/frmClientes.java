    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        if (val.validacionCedula(txtCedula.getText().trim()))
        {
            String cedula, nombre, apellido, direccion, telefono,sangre, presion, observaciones, sexo;
            float peso, talla;
            
            cedula=txtCedula.getText().trim();
            cedulaGlobal=cedula;
            nombre= txtNombre.getText().trim();
            apellido = txtApellido.getText().trim();
            direccion = txtDireccion.getText().trim();
            telefono = txtTelefono.getText().trim();
            sexo = cmbSexo.getSelectedItem().toString();
            observaciones = txtObservaciones.getText().trim();
            
            peso = Float.parseFloat(txtPeso.getText().trim());
            talla = Float.parseFloat(txtTalla.getText().trim());
            
            sangre = cmbGrupoSanguineo.getSelectedItem().toString() + "_" + cmbFactorRH.getSelectedItem().toString();
            presion = txtSistole.getText().trim()+"/"+txtDiastole.getText().trim();
            

            client = new Cliente(cedula, nombre, apellido, direccion, telefono, sangre, presion, observaciones, sexo, peso, talla);
            
            if (clienteDAO.registrarCliente(client))
            {
                this.dispose();
                frmSerologia ser = new frmSerologia();
                ser.setVisible(true);
            }
            limpiarCampos();
            
        }
        else{
            JOptionPane.showMessageDialog(null, "Cedula incorrecta!!");
            txtCedula.setText("");
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

