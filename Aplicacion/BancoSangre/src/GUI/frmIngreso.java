    private void ingreso()
    {
        String [] input = new String[2];
        String [] confirm = new String[2];
        input[0] = txtUsuario.getText().trim();
        input[1] = txtPassword.getText().trim();
        
        confirm = userDAO.ingresarApp(input[0]);
        
        if(input[1].equals(confirm[0]))
        {
            admin = userDAO.comprobarAdmin(input[0]);
            JOptionPane.showMessageDialog(null, "Bienvenido " + confirm[1]);
            frmPresentacion presentacion = new frmPresentacion();
            presentacion.setVisible(true);
            cedulaUsuario = input[0];
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "Acceso Denegado!!");
        }
    }
    
    private void txtUsuarioCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtUsuarioCaretUpdate
        String usuario = txtUsuario.getText().trim();
        
        if(usuario.length()==10 )
            btnIngresar.setEnabled(true);
        else
            btnIngresar.setEnabled(false);
    }//GEN-LAST:event_txtUsuarioCaretUpdate

