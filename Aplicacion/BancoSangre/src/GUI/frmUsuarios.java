    private void btnAceptarModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarModificarActionPerformed
        String codigo = txtCodigoUsuario.getText().trim();
        String nombre = txtNombreUsuario.getText().trim();
        String apellido = txtApellidoUsuario.getText().trim();
        String pass = txtPassUsuario.getText().trim();
        String telefono = txtTelefonoUsuario.getText().trim();
        String sexo = cmbSexoUsuario.getSelectedItem().toString();
        boolean admin = rdAdministradorUsuario.isSelected();
                  
        
        user = new Usuario(codigo, nombre, apellido, pass, telefono, sexo, admin);
        userDAO.modificarUsuario(user);
        limpiarCampos();
        txtCodigoUsuario.setEditable(true);
    }//GEN-LAST:event_btnAceptarModificarActionPerformed

