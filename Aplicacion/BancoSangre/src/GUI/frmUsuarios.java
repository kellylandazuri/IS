    private void limpiarCampos()
    {
        txtCodigoUsuario.setText("");
        txtNombreUsuario.setText("");
        txtApellidoUsuario.setText("");
        txtTelefonoUsuario.setText("");
        txtPassUsuario.setText("");
        txtRepPassUsuario.setText("");
        rdAdministradorUsuario.setSelected(false);
        cmbSexoUsuario.setSelectedIndex(0);
    }
    
    private void confirmarPass()
    {
        String pass, repPass;
        pass = txtPassUsuario.getText().trim();
        repPass = txtRepPassUsuario.getText().trim();
        
        if(pass.equals(repPass))
        {
            btnInsertarUsuario.setEnabled(true);
            btnAceptarModificar.setEnabled(true);
        }
            
        else
        {
            btnInsertarUsuario.setEnabled(false);
            btnAceptarModificar.setEnabled(false);
        }
    }
    
    private void camposVacios()
    {
        String cedula,nombre, apellido, telefono, pass, repPass;
        pass = txtPassUsuario.getText().trim();
        repPass = txtRepPassUsuario.getText().trim();
        cedula = txtCodigoUsuario.getText().trim();
        nombre = txtNombreUsuario.getText().trim();
        apellido = txtApellidoUsuario.getText().trim();
        telefono = txtTelefonoUsuario.getText().trim();
        
        if (pass.isEmpty() || repPass.isEmpty() || cedula.isEmpty() || nombre.isEmpty() || apellido.isEmpty() || telefono.isEmpty())
        {
            btnInsertarUsuario.setEnabled(true);
            btnAceptarModificar.setEnabled(true);
        }
            
        else
        {
            btnInsertarUsuario.setEnabled(false);
            btnAceptarModificar.setEnabled(false);
        }
    }
    
