    private void btnDlgAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDlgAceptarActionPerformed
        String[] datosUsuario = new String[7];
        datosUsuario = userDAO.getDatosUsuario(txtDlgCedula.getText().trim());
        this.setEnabled(true);
        dlgModificar.dispose();
        txtCodigoUsuario.setText(datosUsuario[0]);
        txtCodigoUsuario.setEditable(false);
        txtNombreUsuario.setText(datosUsuario[1]);
        txtApellidoUsuario.setText(datosUsuario[2]);
        txtTelefonoUsuario.setText(datosUsuario[3]);
        txtPassUsuario.setText(datosUsuario[5]);
        
        if(datosUsuario[6].equals("1"))
            rdAdministradorUsuario.setSelected(true);
        else
            rdAdministradorUsuario.setSelected(false);
        
        if(datosUsuario[4].equals("Masculino"))
            cmbSexoUsuario.setSelectedIndex(0);
        else
            cmbSexoUsuario.setSelectedIndex(1);
        
        verBotones(false);
    }//GEN-LAST:event_btnDlgAceptarActionPerformed

