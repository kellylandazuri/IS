    private void mnuReporteTransfucionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuReporteTransfucionActionPerformed
        try {
            Conexion con = new Conexion();
            Connection conn = con.getConexion();
            
            JasperReport reporte = null;
            String path = "src\\Report\\Transfucion.jasper";
            reporte = (JasperReport) JRLoader.loadObjectFromFile(path);
            
            JasperPrint jprint = JasperFillManager.fillReport(path, null, conn);
            
            JasperViewer view = new JasperViewer(jprint, false);
            
            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            
            view.setVisible(true);
            
        } catch (JRException ex) {
            JOptionPane.showMessageDialog(null, "Error al generar reporte!!");
        }
    }//GEN-LAST:event_mnuReporteTransfucionActionPerformed

