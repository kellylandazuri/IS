    private void mnuSerologiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuSerologiaActionPerformed
        try {
            Map parametro = new HashMap();
            Conexion con = new Conexion();
            Connection conn = con.getConexion();
            String path = "src\\Report\\Serologia.jasper";
            JasperReport reporte = (JasperReport) JRLoader.loadObjectFromFile(path);
            String cedula = JOptionPane.showInputDialog(null,"Ingrese la cédula del cliente");
            parametro.put("ced", cedula);
            
            JasperPrint jprint = JasperFillManager.fillReport(path, parametro, conn);
            
            JasperViewer view = new JasperViewer(jprint, false);
            
            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            
            view.setVisible(true);
            
        } catch (JRException ex) {
            Logger.getLogger(frmPresentacion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_mnuSerologiaActionPerformed

