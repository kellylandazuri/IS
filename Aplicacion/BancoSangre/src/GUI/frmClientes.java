    private void btnDlgAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDlgAceptarActionPerformed
        String[] datosCliente = new String[11];
        datosCliente = clienteDAO.getDatosCliente(txtDlgCedula.getText().trim());
        this.setEnabled(true);
        dlgModificar.dispose();
        txtCedula.setText(datosCliente[0]);
        txtCedula.setEditable(false);
        txtNombre.setText(datosCliente[1]);
        txtApellido.setText(datosCliente[2]);
        txtDireccion.setText(datosCliente[3]);
        txtTelefono.setText(datosCliente[4]);
        
        String sexo = datosCliente[5];
        System.out.println(sexo);
        if (sexo.equals("Masculino"))
        {
            cmbSexo.setSelectedIndex(0);
        }
        else
        {
            cmbSexo.setSelectedIndex(1);
        }
        
        txtPeso.setText(datosCliente[6]);   
        txtTalla.setText(datosCliente[7]);
        txtObservaciones.setText(datosCliente[10]);
        
        //sangre
        String[] splitSangre = datosCliente[8].split("_");
        switch (splitSangre[0])
        {
            case "A":   
            {
                cmbGrupoSanguineo.setSelectedIndex(0);
                break;
            }
                
            case "B":   
            {
                cmbGrupoSanguineo.setSelectedIndex(1);
                break;
            }
            
            case "C":   
            {
                cmbGrupoSanguineo.setSelectedIndex(2);
                break;
            }
            
            case "D":   
            {
                cmbGrupoSanguineo.setSelectedIndex(3);
                break;
            }   
        }
        
        switch(splitSangre[1])
        {
            case "+":
                cmbFactorRH.setSelectedIndex(0);
                break;
            case "-":
                cmbFactorRH.setSelectedIndex(1);
        }
        
        //presion
        String[] splitPresion = datosCliente[9].split("/");
        txtSistole.setText(splitPresion[0]);
        txtDiastole.setText(splitPresion[1]);
        verBotones(false);
    }//GEN-LAST:event_btnDlgAceptarActionPerformed

